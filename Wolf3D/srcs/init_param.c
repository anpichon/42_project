static void		map_cpy(int tmp[MH][MW], t_wolf *e)
{
	int		i;
	int		j;

	i = -1;
	while (++i < MH)
	{
		j = -1;
		while (++j < MW)
			e->m.map[i][j] = tmp[i][j];
	}
}

