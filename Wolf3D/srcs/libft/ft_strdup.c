#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	i = 0;
	str = ft_strnew(sizeof(char) * ft_strlen(s1));
	if (str == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[++i] = '\0';
	if (str == NULL)
		return (NULL);
	return (str);
}
